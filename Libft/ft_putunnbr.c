#include "ft_printf.h"

int	ft_putunnbr(long n)
{
	char	c[11];
	size_t	i;

	i = 10;
	c[i] = '\0';
	i -= 1;
	if (n == 0)
		return (write (1, "0", 1));
	while (n != 0)
	{
		c[i] = n % 10 + '0';
		n /= 10;
		i--;
	}
	return (write (1, &c[i + 1], 9 - i));
}
